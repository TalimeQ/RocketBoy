{

}

void ARocketBoyCharacter::Interact()
{
	TArray<AActor*> interactedObjects;
	GetCapsuleComponent()->GetOverlappingActors(interactedObjects);

	for(AActor* interactedObject : interactedObjects)
	{
		AInteractable * currentInteractable  = Cast<AInteractable>(interactedObject);
		if (!currentInteractable) return;
		currentInteractable->OnInteraction();
	
	}
}